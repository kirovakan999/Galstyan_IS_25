#Описать функцию InvertDigits(K), меняющую порядок следования цифр целого положительного числа К на обратный (К — параметр целого типа, являющийся одновременно входным и выходным). С помощью этой функции поменять порядок следования цифр на обратный для каждого из пяти данных целых чисел. Python

def InvertDigits(K):
    return int(str(K)[::-1])  # Преобразуем K в строку, разворачиваем строку и затем возвращаем как целое число

# Пример использования
numbers = [123, 456, 789, 987, 654]

# Поменяем порядок следования цифр на обратный для каждого из пяти чисел
inverted_numbers = [InvertDigits(num) for num in numbers]
print(inverted_numbers)
